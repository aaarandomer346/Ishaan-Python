"""
    123123123/321 returns a float
    to cast to int, do int(123123123/321), this returns an int instead of a float, rounds down
    use round() to round down
    can also use int() to cast strings into ints
"""

print(123123123/321)
print(int(123123123/321))
print(123123123/322)
print(round(123123123/322))
print(int("123123123"))